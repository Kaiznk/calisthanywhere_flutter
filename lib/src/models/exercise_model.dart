class Exercise {
  late String nombre, descripcion, muscle, previos, ayudaA, consejo, nivel, foto;

  Exercise(String nombre, String nivel, String muscle, String previos,
      String ayudaA, String descripcion, String consejo, String foto) {
    this.nombre = nombre;
    this.muscle = muscle;
    this.previos = previos;
    this.ayudaA = ayudaA;
    this.consejo = consejo;
    this.nivel = nivel;
    this.foto = foto;
    this.descripcion = descripcion;
  }

  
}
